print("=== FOR LOOP BASICS ===")

print("\nNumbers from 1 to 5")
for i in range(1, 6):
    print(i)

print("\nPrinting 'Python' 5 times")
for i in range(5):
    print("Python")

print("\nEven numbers from 2 to 10")
for i in range(2, 11, 2):
    print(i)

print("\nOdd numbers from 1 to 9")
for i in range(1, 10, 2):
    print(i)

print("\nLoop Completed Successfully!")
